class CropAdviceService {
  static String getAdvice({
    required String crop,
    required double temp,
    required double humidity,
    required double wind,
    required double rain,
  }) {
    crop = crop.toLowerCase();

    // 🌾 தானியங்கள் (Grains)
    if (crop == "rice" || crop == "நெல்") {
      if (temp < 15) return "⚠️ குறைந்த வெப்பநிலை. பயிர் வளர்ச்சி தாமதமாகும். இரவு நேர குளிர்ச்சியைத் தவிர்க்கவும்.";
      if (temp > 38) return "⚠️ கடும் வெப்பம் (38°C+). நெல் மணி கருவுறுதல் பாதிக்கப்படலாம். வயலில் 5cm நீர் இருக்குமாறு பார்த்துக் கொள்ளவும்.";
      if (temp >= 30 && temp <= 32) return "✅ நெல் வளர்ச்சிக்கு ஏற்ற உகந்த வெப்பநிலை (30-32°C).";
      if (rain > 15) return "🌧 கனமழை! பூக்கும் பருவத்தில் இருந்தால் மகரந்தச் சேர்க்கை பாதிக்கப்படும். வடிகால் அவசியம்.";
    }
    else if (crop == "pearl millet" || crop == "கம்பு" || crop == "sorghum" || crop == "சோளம்" || crop == "ragi" || crop == "ராகி") {
      if (crop.contains("ragi") || crop.contains("ராகி")) {
        if (temp > 40) return "🔥 ராகிக்கு கடும் வெப்பம். நீர் பாய்ச்சுவது அவசியம்.";
        if (temp < 15) return "⚠️ குளிரால் ராகி வளர்ச்சி பாதிக்கப்படலாம்.";
      }
      if (rain > 20) return "⚠️ இந்த சிறுதானியங்களுக்கு அதிக நீர் தேங்குவது ஆபத்து. வடிகால் வசதியைச் சரிசெய்யவும்.";
      if (temp > 40) return "🔥 வெயிலின் தாக்கம் அதிகம், இலேசான நீர் பாய்ச்சுவது நல்லது.";
    }
    else if (crop == "black gram" || crop == "உளுந்து") {
      if (rain > 10) return "⚠️ உளுந்து பூக்கும் தருணத்தில் மழை பெய்தால் பூக்கள் உதிரலாம். முன்னெச்சரிக்கை தேவை.";
      if (humidity > 80) return "🦠 அதிக ஈரப்பதம் மஞ்சள் தேமல் நோயை பரப்பலாம். வயலை கண்காணிக்கவும்.";
    }

    // 🌿 பணப்பயிர்கள் (Cash Crops)
    else if (crop == "sugarcane" || crop == "கரும்பு") {
      if (temp >= 27 && temp <= 33) return "✅ கரும்பு வளர்ச்சிக்கு ஏற்ற உகந்த வெப்பநிலை (27-33°C).";
      if (wind > 45) return "🌪 பலத்த காற்று கரும்பைச் சாய்க்க வாய்ப்புள்ளது. முட்டுக்கட்டை அவசியம்.";
      if (rain > 15) return "✅ கரும்புக்கு இந்த மழை மிகவும் நல்லது. நீர் தேங்காமல் பார்த்துக் கொள்ளவும்.";
      if (humidity > 80) return "✅ அதிக ஈரப்பதம் கரும்பு தண்டு வளர்ச்சிக்கு (Elongation) உதவும்.";
    }
    else if (crop == "cotton" || crop == "பருத்தி") {
      if (rain > 10) return "⚠️ பருத்தி வெடிக்கும் தருவாயில் மழை பெய்தால் தரம் குறையும். காய்களை முன்கூட்டியே பறிக்கவும்.";
      if (humidity > 75) return "🦠 அதிக ஈரப்பதம் பருத்தியில் காய்ப்புழு தாக்குதலை அதிகரிக்கும்.";
    }
    else if (crop == "groundnut" || crop == "நிலக்கடலை") {
      if (rain > 15) return "⚠️ நிலக்கடலை அறுவடை சமயமாக இருந்தால் மழையினால் முளைக்க வாய்ப்புள்ளது.";
      if (temp > 35 && rain == 0) return "🔥 வறட்சியான சூழல். கடலை திரட்சியாக வளர நீர் பாய்ச்சுவது அவசியம்.";
    }

    // 🍎 பழங்கள் & காயறிகள் (Fruits & Veggies)
    else if (crop == "banana" || crop == "வாழை") {
      if (temp < 20) return "⚠️ குறைந்த வெப்பநிலை. வாழை வளர்ச்சி மெதுவாகும் (20°C-க்கு கீழ்).";
      if (temp < 8) return "🚨 கடும் குளிர்! வாழைக்கு பெரும் சேதம் ஏற்படலாம்.";
      if (temp > 35) return "🔥 அதிக வெப்பம் (35°C+). இலைகள் கருகலாம். நீர் மேலாண்மை அவசியம்.";
      if (wind > 30) return "🌪 பலத்த காற்று! வாழை மரங்கள் சாயாமல் இருக்க முட்டுக்கட்டை (Props) கொடுக்கவும்.";
    }
    else if (crop == "mango" || crop == "மா") {
      if (temp > 28 && humidity > 75) return "⚠️ மா மரம் பூக்கும் தருணத்தில் பூக்கள் உதிரும் மற்றும் தத்துப்பூச்சி தாக்குதல் வரலாம்.";
      if (wind > 40) return "🌪 பலத்த காற்றால் பிஞ்சுகள் உதிர வாய்ப்புள்ளது.";
    }
    else if (crop == "tomato" || crop == "தக்காளி" || crop == "onion" || crop == "வெங்காயம்") {
      if (rain > 15) return "⚠️ தொடர் மழையால் வேர் அழுகல் நோய் வரலாம். வடிகால் மிக அவசியம்.";
      if (temp > 38) return "🔥 தக்காளி/வெங்காய பிஞ்சுகள் வெயிலில் கருகாமல் இருக்க மாலை நேரத்தில் நீர் பாய்ச்சவும்.";
    }

    // 🌴 மரங்கள் (Trees & Plantations)
    else if (crop == "coconut" || crop == "தென்னை") {
      if (temp < 21) return "⚠️ வெப்பநிலை 21°C-க்கு கீழ் உள்ளதால் தேங்காய் விளைச்சல் குறையலாம்.";
      if (temp >= 27) return "✅ தென்னை வளர்ச்சிக்கு உகந்த வெப்பநிலை (27°C).";
      if (wind > 60) return "🌪 கடும் சூறாவளி காற்று எச்சரிக்கை. தென்னை மரங்கள் பாதிக்கப்படலாம்.";
      if (humidity > 80) return "🦠 அதிக ஈரப்பதம் பூச்சி மற்றும் நோய் தாக்குதலை அதிகரிக்கும். கண்காணிப்பு தேவை.";
    }
    else if (crop == "cashew" || crop == "முந்திரி" || crop == "teak" || crop == "தேக்கு" || crop == "casuarina" || crop == "சவுக்கு") {
      if (wind > 50) return "🌪 பலத்த காற்றினால் கிளைகள் முறிய வாய்ப்புள்ளது.";
      if (rain > 20) return "✅ மர வகைகளுக்கு இந்த மழைப்பொழிவு வளர்ச்சிக்கு மிகவும் உதவும்.";
    }

    // 🐄 கால்நடைகள் (Cattle / Livestock)
    else if (crop == "cattle" || crop == "கால்நடைகள்") {
      // Temperature-Humidity Index (THI) calculation
      // Formula: THI = 0.8 * T + [RH/100 * (T - 14.4)] + 46.4
      double thi = (0.8 * temp) + ((humidity / 100) * (temp - 14.4)) + 46.4;
      
      if (thi > 89) return "🚨 உயிருக்கு ஆபத்தான வெப்பம் (Critical)! கால்நடைகளை குளிர்ந்த நிலையில் வைக்கவும், மின்விசிறி மற்றும் நீர் தெளிப்பான்களைப் பயன்படுத்தவும்.";
      if (thi > 79) return "🔥 கடும் வெப்ப அழுத்தம் (Severe Stress)! கால்நடைகளுக்கு அதிகக் குளிர்ந்த நீர் கொடுக்கவும். நிழலில் கட்டி வைக்கவும்.";
      if (thi > 71) return "⚠️ மிதமான வெப்ப அழுத்தம். போதுமான காற்றோட்டம் மற்றும் தண்ணீர் வசதி செய்யவும்.";
      if (thi > 68) return "💡 லேசான வெப்ப அழுத்தம் ஆரம்பம். கவனிக்கவும்.";
      return "✅ கால்நடைகளுக்கு உகந்த குளுமையான சூழல் (Comfort Zone).";
    }

    return "✅ தற்போதைய வானிலை உங்கள் பயிருக்கு ஏற்றதாக உள்ளது.";
  }
}
